// Bakes the desktop's file-icon artwork + mappings into a module the Expo app can
// import. mobile/ is a separate package with its own Metro root, so it can't reach
// up into src/renderer/ at build time — but the icons and the extension tables
// should not be maintained twice. So they're generated from the desktop's own
// modules, and test/mobile-assets.test.mjs fails if the checked-in output drifts
// from what this script would write.
//
//   node scripts/gen-mobile-assets.mjs   (or: npm run gen:mobile)
//
// Sources: src/renderer/shared/file-icons.js (EXT_ICON/NAME_ICON), the SVGs they
// name in src/renderer/file-icons/, and EXT_LANG from shared/highlight.js.

import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { EXT_ICON, NAME_ICON, FILE_ICON_IDS } from '../src/renderer/shared/file-icons.js';
import { EXT_LANG } from '../src/renderer/shared/highlight.js';

const ICON_DIR = new URL('../src/renderer/file-icons/', import.meta.url);
export const OUT_FILE = new URL('../mobile/src/generated/desktop-assets.ts', import.meta.url);

// The Lucide glyphs are pretty-printed across a dozen lines; inlined as string
// literals that whitespace is pure bundle weight. The artwork is untouched.
function squash(svg) {
  return svg.replace(/\s*\n\s*/g, ' ').replace(/\s{2,}/g, ' ').trim();
}

const lit = (s) => JSON.stringify(s);
const table = (obj) => Object.entries(obj).map(([k, v]) => `  ${lit(k)}: ${lit(v)},`).join('\n');

export function renderModule() {
  const svgs = readdirSync(ICON_DIR)
    .filter((f) => f.endsWith('.svg'))
    .map((f) => [f.slice(0, -4), squash(readFileSync(new URL(f, ICON_DIR), 'utf8'))])
    .filter(([id]) => FILE_ICON_IDS.has(id))
    .sort(([a], [b]) => a.localeCompare(b));

  return `// GENERATED by scripts/gen-mobile-assets.mjs — do not edit by hand.
// Run \`npm run gen:mobile\` after changing src/renderer/shared/file-icons.js,
// shared/highlight.js, or the SVGs in src/renderer/file-icons/.
//
// The same artwork and the same extension tables the desktop explorer uses, so a
// .ts file shows the TypeScript logo on both, and a .py highlights as Python on
// both. ICON_SVG holds raw SVG markup (self-colored — no tinting needed), painted
// by react-native-svg's SvgXml.

const EXT_ICON: Record<string, string> = {
${table(EXT_ICON)}
};

const NAME_ICON: Record<string, string> = {
${table(NAME_ICON)}
};

// extension -> highlight.js language id (the desktop's shared/highlight.js map).
// An unmapped extension means "let hljs auto-detect", same as the desktop viewer.
const EXT_LANG: Record<string, string> = {
${table(EXT_LANG)}
};

export const ICON_SVG: Record<string, string> = {
${svgs.map(([id, svg]) => `  ${lit(id)}: ${lit(svg)},`).join('\n')}
};

const extOf = (file: string) => {
  const m = /\\.([^.]+)$/.exec(file);
  return m ? m[1].toLowerCase() : '';
};

const baseName = (name: string) => String(name).split('/').pop()!.toLowerCase();

export function iconForFile(name: string): string {
  const base = baseName(name);
  return NAME_ICON[base] || EXT_ICON[extOf(base)] || 'file';
}

export function iconForFolder(open = false): string {
  return open ? 'folder-open' : 'folder';
}

// null -> no grammar for this extension; the viewer falls back to auto-detection.
export function langFor(file: string): string | null {
  return EXT_LANG[extOf(file)] ?? null;
}
`;
}

// Only write when run directly — the test imports renderModule() to compare.
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  writeFileSync(OUT_FILE, renderModule());
  console.log(`wrote ${fileURLToPath(OUT_FILE)}`);
}
